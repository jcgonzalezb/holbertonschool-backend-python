#!/usr/bin/env python3
"""
This project module contains a type-annotated function that takes two
float arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    This is a method that takes two float arguments
    and returns their sum as a float.
    Returns:
        Sum of two arguments.
    """
    return (a + b)
